package com.ict.shopping.model.vo;

public class WishVO {
	private String client_num, prod_num, wish_st, wish_in;

	public String getClient_num() {
		return client_num;
	}

	public void setClient_num(String client_num) {
		this.client_num = client_num;
	}

	public String getProd_num() {
		return prod_num;
	}

	public void setProd_num(String prod_num) {
		this.prod_num = prod_num;
	}

	public String getWish_st() {
		return wish_st;
	}

	public void setWish_st(String wish_st) {
		this.wish_st = wish_st;
	}

	public String getWish_in() {
		return wish_in;
	}

	public void setWish_in(String wish_in) {
		this.wish_in = wish_in;
	}
}
