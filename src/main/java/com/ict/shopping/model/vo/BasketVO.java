package com.ict.shopping.model.vo;

public class BasketVO {
	private String cart_num, prod_num, cart_amount, cart_price, cart_st, review_st, client_num;
	
	public String getCart_num() {
		return cart_num;
	}

	public void setCart_num(String cart_num) {
		this.cart_num = cart_num;
	}

	public String getReview_st() {
		return review_st;
	}

	public void setReview_st(String review_st) {
		this.review_st = review_st;
	}

	public String getProd_num() {
		return prod_num;
	}

	public void setProd_num(String prod_num) {
		this.prod_num = prod_num;
	}

	public String getCart_amount() {
		return cart_amount;
	}

	public void setCart_amount(String cart_amount) {
		this.cart_amount = cart_amount;
	}

	public String getCart_price() {
		return cart_price;
	}

	public void setCart_price(String cart_price) {
		this.cart_price = cart_price;
	}

	public String getCart_st() {
		return cart_st;
	}

	public void setCart_st(String cart_st) {
		this.cart_st = cart_st;
	}

	public String getClient_num() {
		return client_num;
	}

	public void setClient_num(String client_num) {
		this.client_num = client_num;
	}
}
